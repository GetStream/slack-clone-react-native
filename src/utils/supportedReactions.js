export const getSupportedReactions = (grouped = false, groupSize = 6) => {
  if (grouped) {
    const reactions = [];

    Object.keys(reactionData).forEach((k) => {
      const data = [];

      for (let i = 0; i < reactionData[k].length; i = i + groupSize) {
        data.push(reactionData[k].slice(i, i + groupSize));
      }

      reactions.push({
        data,
        title: k,
      });
    });
    return reactions;
  }

  let reactions = [];

  Object.keys(reactionData).forEach((k) => {
    if (k === 'Frequently used') {
      return;
    }
    const sr = reactionData[k].map((r) => ({
      Icon: r.icon,
      type: r.id,
    }));
    reactions = reactions.concat(sr);
  });

  return reactions;
};

